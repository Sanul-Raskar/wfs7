package rsa;

/**
 *
 * @author surya
 */
public class Employee {
    private int age;
    private String name;

    public Employee(int quantity, String name) {
        this.age = quantity;
        this.name = name;
    }

    public Employee() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
